// EJERCICIO 13: Mostrar todos los elementos de un arreglo
function mostrarArreglo() {

    // Se crea un arreglo llamado 'numeros' con 5 elementos
    let numeros = [10, 20, 30, 40, 50];

    // Bucle que recorre el arreglo desde la posición 0 hasta el final
    for (let i = 0; i < numeros.length; i++) {
        console.log("Número en posición " + i + " es: " + numeros[i]);
    }
}
